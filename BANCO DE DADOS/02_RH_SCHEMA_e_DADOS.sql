CREATE SCHEMA RH AUTHORIZATION POSTGRES;

SET SEARCH_PATH TO RH;

CREATE TABLE DEPT(
  COD_DEPT NUMERIC(2,0),
  NOME    VARCHAR(14),
  CIDADE  VARCHAR(13),
  CONSTRAINT PK_DEPT PRIMARY KEY (COD_DEPT));

CREATE TABLE FUNC(
  COD_FUNC     NUMERIC(4,0),
  NOME         VARCHAR(10),
  CARGO        VARCHAR(10),
  COD_GER      NUMERIC(4,0),
  ADMISSAO     DATE,
  SALARIO      NUMERIC(7,2),
  COMISSAO     NUMERIC(7,2),
  COD_DEPT     NUMERIC(2,0),
  CONSTRAINT PK_FUNC PRIMARY KEY (COD_FUNC),
  CONSTRAINT FK_FUNC_DEPT FOREIGN KEY (COD_DEPT) REFERENCES DEPT(COD_DEPT),
  CONSTRAINT FK_FUNC_FUNC FOREIGN KEY (COD_GER)  REFERENCES FUNC(COD_FUNC)
);

CREATE TABLE FAIXASAL (
  FAIXA NUMERIC,
  MENOR NUMERIC,
  MAIOR NUMERIC);

