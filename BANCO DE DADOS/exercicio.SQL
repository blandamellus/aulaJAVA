SELECT F.NOME, F, COD_DEPT, D.NOME
FROM FUNC F
	JOIN DEPT D ON F.COD_DEPT = D.COD_DEPT

SELECT F.NOME, D.NOME, F.COMISSAO
FROM FUNC F
JOIN DEPT D ON F.COD_DEPT = D.COD_DEPT
WHERE F.COMISSAO IS NOT NULL 

SELECT F.NOME, D.NOME
FROM FUNC F 
JOIN DEPT D ON F.COD_DEPT = D.COD_DEPT
WHERE F.NOME LIKE 'A%'

SELECT F.NOME, F.CARGO, D.NOME, D.COD_DEPT, D.CIDADE
FROM FUNC F 
JOIN DEPT D ON F.COD_DEPT = D.COD_DEPT
WHERE D.CIDADE LIKE 'DALLAS'

SELECT F.NOME, F.COD_GER, F.COD_FUNC, D.COD_DEPT, F.CARGO,
	D.NOME, GER.NOME AS GERENTE
FROM FUNC F
JOIN DEPT D ON F.COD_DEPT = D.COD_DEPT
JOIN FUNC GER ON GER.COD_FUNC = F.COD_GER