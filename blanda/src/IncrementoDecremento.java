public class IncrementoDecremento {
    public static void main(String[] args) throws Exception {
        
        int numero = 3;

        numero = numero + 1;
        System.out.println(numero);
        
        
        System.out.println("incremento: "+ ++numero);
        System.out.println("decremento: "+ --numero);

        


        
  
    }
}